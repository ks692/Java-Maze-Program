package player;

/**
 * Represents the possible Treasure item that can be collected by the player.
 * Treasure Items that can be used.
 * {@link #DIAMOND}
 * {@link #RUBY}
 * {@link #SAPPHIRE}
 */
public enum Treasure {
  /**
   * Represents Diamond as a treasure Item
   * that can be collected.
   */
  DIAMOND,
  /**
   * Represents Ruby as a treasure Item
   * that can be collected.
   */
  RUBY,
  /**
   * Represents Sapphire as a treasure Item
   * that can be collected.
   */
  SAPPHIRE
}
